#!/usr/bin/env node
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, join } from "node:path";

const repoRoot = new URL("..", import.meta.url).pathname.replace(/\/$/, "");
const glyphsetsRoot =
  process.env.GF_GLYPHSETS_REPO ?? "/Users/eli/GF/repos/glyphsets";
const definitionsDir = join(glyphsetsRoot, "Lib/glyphsets/definitions");
const niceNamesDir = join(glyphsetsRoot, "data/results/txt/nice-names");
const outPath = join(repoRoot, "web/src/gfSidebarData.generated.ts");

function readYamlValue(text, key) {
  const match = text.match(new RegExp(`^${key}:\\s*\"?([^\"\\n]+)\"?`, "m"));
  return match?.[1]?.trim() ?? "";
}

function readLanguageCodes(text) {
  const lines = text.split(/\r?\n/);
  const codes = [];
  let inList = false;
  for (const line of lines) {
    if (/^language_codes:\s*$/.test(line)) {
      inList = true;
      continue;
    }
    if (inList && /^\S/.test(line)) break;
    const match = line.match(/^\s*-\s*([A-Za-z0-9_]+)/);
    if (match) codes.push(match[1]);
  }
  return codes;
}

function fallbackScript(id) {
  if (id.includes("_Latin_")) return "Latin";
  if (id.includes("_Arabic_")) return "Arabic";
  if (id.includes("_Cyrillic_")) return "Cyrillic";
  if (id.includes("_Greek_")) return "Greek";
  if (id.includes("_Phonetics_")) return "Phonetics";
  if (id.includes("_TransLatin_")) return "TransLatin";
  return "Other";
}

function labelFor(id) {
  return id.replace(/^GF_/, "GF ").replaceAll("_", " ");
}

const glyphsets = [];
for (const filename of readdirSync(niceNamesDir).sort()) {
  if (!filename.endsWith(".txt")) continue;
  const id = basename(filename, ".txt");
  const names = readFileSync(join(niceNamesDir, filename), "utf8")
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("#"));
  const yamlPath = join(definitionsDir, `${id}.yaml`);
  let script = fallbackScript(id);
  let description = "";
  let languageCodes = [];
  try {
    const yaml = readFileSync(yamlPath, "utf8");
    script = readYamlValue(yaml, "script") || script;
    description = readYamlValue(yaml, "description");
    languageCodes = readLanguageCodes(yaml);
  } catch {
    // Some future generated files may not have matching definitions.
  }
  glyphsets.push({
    id,
    label: labelFor(id),
    script,
    description,
    languageCodes,
    glyphNames: names,
    expectedCount: names.length,
  });
}

const header = `// Generated by scripts/generate-gf-sidebar-data.mjs.\n// Source: ${glyphsetsRoot}\n// Do not edit by hand.\n\n`;
writeFileSync(
  outPath,
  `${header}export const GF_GLYPHSETS = ${JSON.stringify(glyphsets, null, 2)} as const;\n`,
);
console.log(`wrote ${outPath} (${glyphsets.length} glyphsets)`);
